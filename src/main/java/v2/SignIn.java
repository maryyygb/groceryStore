package v2;

public class SignIn {

    private int idRole;
    private String roleName;

    public SignIn() {
        
    }

    public void setIdRole(int id) {
        this.idRole = id;
    }

    public int getIdRole() {
        return this.idRole;
    }

    public void setRole(String role) {
        this.roleName = role;
    }

    public String getRole() {
        return this.roleName;
    }


}
